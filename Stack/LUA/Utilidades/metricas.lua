-- Tempo:
-- Um dia tem 24 horas, que é igual a 86.400 segundos (24 horas * 60 minutos * 60 segundos).
-- Um mês tem em média 30.44 dias.
-- Um ano comum tem 365 dias.
-- Um ano bissexto tem 366 dias.

-- Conversões de Tempo:
-- Um segundo tem 1.000 milissegundos.
-- Um minuto tem 60 segundos, o que equivale a 60.000 milissegundos.
-- Uma hora tem 3.600 segundos, que é igual a 3.600.000 milissegundos.

-- Distância:
-- 1 quilômetro (km) equivale a 1.000 metros.